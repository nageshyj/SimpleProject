package com.amadeus.simple.project;

/**
 * Calculator.
 * 
 * @author njayarama
 *
 */
public class Calculator {

	/**
	 * Add
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public long addValues(int a, int b) {
		return a + b;
	}
	
	/**
	 * Multiply 
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public long multiplyValues(int a, int b) {
		return a * b;
	}
}
